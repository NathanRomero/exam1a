package joseromero.exam1a.externalrequests.weatherApi.schemas;

public class wind {
    public float speed;
    public float deg;
    public float gust;

    public float getSpeed() {
        return this.speed;
    }

    public void setSpeed(float speed) {
        this.speed = speed;
    }

    public float getDeg() {
        return this.deg;
    }

    public void setDeg(float deg) {
        this.deg = deg;
    }

    public float getGust() {
        return this.gust;
    }

    public void setGust(float gust) {
        this.gust = gust;
    }

   
}
